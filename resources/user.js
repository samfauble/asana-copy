const { nameIdKey } = require('../utils/util')

// get a list of users
const performList = async (z, bundle) => {
  const response = await z.request({
    url: 'https://app.asana.com/api/1.0/users',
    params: {
      order_by: 'id desc'
    }
  });
  nameIdKey(response.data.data, 'gid')
  return response.data.data
};

